#!/bin/bash
[[ ${VERBOSE} ]] && set -x
_CUR_FILE_DIR_="$(cd "$(dirname "$0")"; pwd -P)"
_FILENAME_="$(basename -- "${0}")"
_CUR_FILE_PATH_="${_CUR_FILE_DIR_}/${_FILENAME_}"
HN="$(hostname | sed 's/.cisco.com//g')"
FULL_LOG=""
_build_clab_component_script_="$(realpath ${_CUR_FILE_DIR_}/build_clab_component.sh)"

_build_folder_="$(realpath ${_CUR_FILE_DIR_}/../../build)"

_template_folder_="$(realpath ${_CUR_FILE_DIR_}/template)"

_docker_folder_="$(realpath ${_CUR_FILE_DIR_}/../../docker)"
_ovxr_dev_df_="${_docker_folder_}/Dockerfile.dev"
_clab_docker_folder_="${_docker_folder_}/clab"

_integration_folder_="$(realpath ${_CUR_FILE_DIR_}/../../packages/integration/clab)"

_scripts_folder_="$(realpath ${_CUR_FILE_DIR_}/..)"
_docker_build_script_="${_scripts_folder_}/build_docker_image.sh"

_packages_folder_="$(realpath ${_CUR_FILE_DIR_}/../../packages/)"
_images_folder_="${_packages_folder_}/images/8000/clab"
mkdir -p ${_images_folder_}

_allowed_plats_file_="${_CUR_FILE_DIR_}/allowed_plats"
_allowed_plats_="$(< ${_allowed_plats_file_})"

NO_BAKE=1

#FILE AGE CHECK START#
#isItOldEnough "/path/to/file" "age in minutes"
#Return: 0 if it is old enough, 1 if not
function isItOldEnough() {
  _FILE_=$1
  _AGE_=$2
  [[ "${_AGE_}" == "" ]] && _AGE_="720"
  if [[ -e "${_FILE_}" ]];
  then
    if [[ "$(find ${_FILE_} -mmin +${_AGE_})" != "" ]];
    then
      return 0
    fi
  fi
  return 1
}
#FILE AGE CHECK END#

#LOK START#
mkdir -p /tmp/l0ks
_LOK='/tmp/l0ks/enter_lok_name'
function getLock() {
  if [[ -f "${_LOK}" ]]; then
    if [[ "$(find ${_LOK} -mmin +10)" == "" ]]; then
      #Lok file exists and has not passed 10minutes#
      return 1
    fi
  fi
  #No lok file, get it
  touch "${_LOK}" > /dev/null 2>&1
	if [ $? -ne 0 ];
	then
		outLog "Could not touch ${_LOK}, may be permission issue"
		return 1
	fi
  outLog "acquired lock"
  return 0
}

function releaseLock() {
  /usr/bin/rm -rf "${_LOK}"
  outLog "released lock"
  return 0
}
#LOK END#

#LOG START#
# $1 - Output message
function outLog {
  MSG="${1}"
  TDATE=$(/bin/date +'%Y-%m-%d %T')
  OPT=$(echo -e "[${TDATE}] ${MSG}")
  echo "${OPT}"
  appendFullLog ${OPT}
  return
}
#LOG END#

#FULL LOG START#
function appendFullLog() {
  INPT="$@"
  FULL_LOG="${FULL_LOG}
${INPT}"
  return
}
#FULL LOG END#

# SEND EMAIL START#
function sendEmail() {
  _SUBJECT="${1}"
  _MSG="${2}"
  _TO="${3}"
  _TRACE_FILE="${4}"
  _INTERVAL="${5}" # Interval in minutes of how often an email should be sent out
	_MSG="${_MSG}
	This email was auto-generated by $(hostname):${_CUR_FILE_PATH}
	This email will be sent out again in ${_INTERVAL} minutes if problem continues
	"
  if [[ ( -e "${_TRACE_FILE}" ) && ( "$(find ${_TRACE_FILE} -mmin +${_INTERVAL})" == "" ) ]]; then
    # Will wait another hour until a new message is sent out
    outLog "Message has already been sent out in the last ${_INTERVAL} minutes"
    outLog "Trace File - ${_TRACE_FILE}"
    return 1
  fi
  touch ${_TRACE_FILE}
  outLog "${_MSG}" |  mail -s "${_SUBJECT}" "${_TO}"
  outLog "Email sent out to ${_TO}"
  outLog "Trace File - ${_TRACE_FILE}"
  return
}

# SEND EMAIL END  #

# DRAIN CHCEK START #
function drainKeywordCheck() {
  # Returns 0 if keyword is in drain reason
  # else returns 1
  keyword=${1}
  OPT="$(sinfo -n ${HN} -R | sed 1d)"
  [[ $(grep "${keyword}" <<< "${OPT}") ]] && return 0 || return 1
}
# DRAIN CHECK END   #

# HELP MESSAGE START #
function help() {
  msg="
  This script is the starting point for users to build their own clab containers with their own provided ISO.

  Parameters:
    -i	/path/to/iso	      User path to iso build clab container off of
    -p plat_to_build	      Allowed plats: ${_allowed_plats_}
    -o			      Pass -o if you are calling this script outside of ovxr-dev. Exclude it otherwise

  Arguments:
    preferred_container_name    MUST BE ALL LOWER CASE

  Usage:
    ISO				= /nobackup/8000-x64.iso
    PLAT			= 8201-sys
    Preferred Docker image name	= c8201:latest

    ./${_FILENAME_} -i /nobackup/8000-x64.iso -p 8201-sys -o c8201:latest 

  "
  echo "${msg}"
  return
}

function fail_exit() {
  echo "Last step failed.. re-run this docker container passing in -e VERBOSE=\"TRUE\""
  exit
}

# HELP MESSAGE END #
# OPTIONAL ARGUMENTS START #
while getopts "oi:p:hn" opt;do
	case $opt in
	i)
		ISO_TO_BAKE="${OPTARG}"
		[ ! -e ${ISO_TO_BAKE} ] && "${ISO_TO_BAKE} is not a valid path" && exit 1
    ISO_TO_BAKE="$(realpath ${OPTARG})"
    ISO_TO_BAKE_NAME=$(basename -- ${ISO_TO_BAKE})
    ISO_TO_BAKE_FOLDER="$(cd "$(dirname "${ISO_TO_BAKE}")"; pwd -P)"
		;;
	p)
		PLAT_TO_BUILD="${OPTARG}"
		grep -q ${PLAT_TO_BUILD} <<< "${_allowed_plats_}"
		[[ $? -ne 0 ]] && echo "Please pass in valid plat to -p. Allowed plats: ${_allowed_plats_}" && exit 1
		export PLAT_BUILD=${PLAT_TO_BUILD}
		;;
	h)
		help
		exit 0
		;;
  n)
    NO_BAKE=0
    ;;
	o)
		OUTSIDE_OVXR_DEV="TRUE"
		;;
	\?)
		echo "FILLER"
		exit
		;;
	esac
done
shift $((OPTIND-1))
# OPTIONAL ARGUMENTS   END #


# MAIN #
echo "${_FILENAME_} called"
outLog "Running prechecks"
[[ ! ${1} ]] && help && echo "Please pass in docker name to use. Read above for help" && exit 1
DOCKER_NAME="${1}"

[[ ( ${OUTSIDE_OVXR_DEV} ) &&  ( ! ${ISO_TO_BAKE} ) ]] && help && echo "Please specify which ISO to bake using -i. Refer to the help message above for more details" && exit 1

_cp_cmd_=$(which cp)

[ ! ${PLAT_TO_BUILD} ] && help && echo "Please specify platform to build: 8101-32H, 8101-32FH, 8102-64H, 8201-32FH, 8201-sys, 8202, 8202-32FH-M, 8201-24H8FH, 8804, 8808-gb" && exit 1
[ ! -e ${_ovxr_dev_df_} ] && echo "Missing ${_ovxr_dev_df_} It should be included with the release" && exit 1
[ ! -e ${_docker_build_script_} ] && echo "Missing ${_docker_build_script_} It should be included with the release" && exit 1
[ ! -e ${_build_clab_component_script_} ] && echo "Missing ${_build_clab_component_script_} It should be included with the release" && exit 1

if [[ ( ${OUTSIDE_OVXR_DEV} ) && ( ${NO_BAKE} -eq 1 ) ]];
then
  #create ovxr-dev:latest docker image, which is required for baking#
  _ovxr_dev_docker_name_="ovxr-dev:latest"
  docker image inspect ${_ovxr_dev_docker_name_} > /dev/null 2>&1
  if [[ $? -ne 0 ]];
  then
    outLog "Creating ovxr-dev dockerimage since it was not found - which is required to create the qcow2 disk"
    outLog "If you have ${_ovxr_dev_docker_name_} already, but under a different name, please retag it to ovxr-dev:latest and re-run this script"
    sleep 5s
    _cmd_="${_docker_build_script_} ${_ovxr_dev_docker_name_} ${_ovxr_dev_df_}"
    outLog "cmd to build ovxr-dev:latest: \$ ${_cmd_}"
    eval ${_cmd_}
    [ $? -ne 0 ] && fail_exit
  else
    outLog "Using the existing ${_ovxr_dev_docker_name_}"
  fi

  #create bakeme_# folder to use for baking#
  outLog "Creating bake folder. This is where the baked qcow2 will be placed."
  _a_=1
  _bake_folder_="${_build_folder_}/bakeme_${_a_}"
  while true;
  do
    [ ! -e ${_bake_folder_} ] && mkdir -p ${_bake_folder_} && break
    ((_a_++))
    _bake_folder_="${_bake_folder_::-1}""${_a_}"
  done
  outLog "bake folder: ${_bake_folder_}"

  #copy user specified iso to bakeme_# folder#
  _iso_drop_path_="${_bake_folder_}/8000-x64.iso"
  outLog "copying ${ISO_TO_BAKE} to ${_iso_drop_path_}"
  ${_cp_cmd_} ${ISO_TO_BAKE} ${_iso_drop_path_}
  [ $? -ne 0 ] && fail_exit
else
  outLog "NOTICE: -o was not passed in so assuming we are running inside an ovxr-dev container. If this is not the case, exit immediately and call again with -o"
  sleep 4s
fi

#bake iso#
if [[ ! ${OUTSIDE_OVXR_DEV} ]];
then 
  _bake_folder_="/nobackup/bake"
  _iso_drop_path_=""
  for f in $(realpath $(find "${_bake_folder_}"/* 2>/dev/null) 2>/dev/null); #set iso path
  do
    _ext_="${f##*.}"
    if [[ "${_ext_}" == "iso" ]];
    then
      _iso_drop_path_=${f}
      break
    fi
  done
#  [[ "${_iso_drop_path_}" == "" ]] && outLog "Was not able to find an ISO.. Exiting" && exit 1
  if [[ "${_iso_drop_path_}" == "" ]];
  then
    mkdir -p /nobackup/bake
    _cp_cmd_=$(which cp)
    ${_cp_cmd_} ${ISO_TO_BAKE} /nobackup/bake/8000-x64.iso
    [[ -e /nobackup/bake/8000-x64.iso ]] && _iso_drop_path_="/nobackup/bake/8000-x64.iso"
  fi
  [[ "${_iso_drop_path_}" == "" ]] && outLog "Was not able to find an ISO.. Exiting" && exit 1
fi
if [[ ${NO_BAKE} -eq 1 ]]; #skip bake if -n was passed in
then
  if [[ ${OUTSIDE_OVXR_DEV} ]];
  then
    #TODO: create isoboot flow for here
    _cmd_="docker run --rm -it --privileged -e ACTION=bake -e PLAT_BUILD=${PLAT_TO_BUILD} -v ${_bake_folder_}:/nobackup/bake ${_ovxr_dev_docker_name_}"
    outLog "Calling ovxr-dev to bake ${_iso_drop_path_}"
    outLog "cmd: ${_cmd_}"
    eval ${_cmd_}
    [ $? -ne 0 ] && echo "Creating prebake disk failed !!! Cannot continue to create the docker component for this reason." && exit 1
  else
    #INSIDE OVXR DEV CONTAINER#
    [ $UID -ne 0 ] && outLog "ERROR: Please re-run as root or call using sudo" && exit 1
    _bake_folder_="/nobackup/bake"
    #If qcow2 is present, we'll skip creating the precook disk and go straight to buildig the clab component#
    _SKIP_=1
    if [[ "${PLAT_BUILD::2}" == "88" ]];
    then
      [[ ( $(ls ${_bake_folder_}/8808/rp/hda) ) && ( $(ls ${_bake_folder_}/8808/lc/hda) ) && ( $(ls ${_bake_folder_}/*.iso) ) ]] && outLog "Found existing sf-d precook artifacts, skipping bake" && _SKIP_=0
    else
      [[ ( $(ls ${_bake_folder_}/*.qcow2 2>/dev/null) ) ]] && outLog "Found existing sf-f precook artifacts, skipping bake" && _SKIP_=0
    fi
    
    if [[ ${_SKIP_} -ne 0 ]];
    then
      /etc/bake_in_container_startup.sh
      if [ $? -ne 0 ];
      then
        outLog "ERROR: failed to bake ISO for the reason above. Exiting"
        exit 1
      fi
    fi
  fi



  _FAIL_ON_=1
  outLog "ISO & QCOW2 in ${_bake_folder_}:
  $(ls ${_bake_folder_})"
  if [[ $(ls -l ${_bake_folder_} | sed 1d | wc -l) -eq 3 ]];
  then
    if [[ ! $(ls ${_bake_folder_} | grep ${ISO_TO_BAKE_NAME}.patched) ]];
    then
      _FAIL_ON_=0
    fi
  else
    if [[ $(ls -l ${_bake_folder_} | sed 1d | wc -l) -gt 2 ]];
    then
      _FAIL_ON_=0
    fi
  fi
  if [[ ${_FAIL_ON_} -eq 0 ]];
  then
    outLog "WARNING: After baking, there should only be 2 objects in the folder where the original iso is located at:
    - initial iso
    - baked file or folder w/ the qcow2, in the case of sf-d

    The only exception to this is if the passed in ISO had the wrong SDK defined in its sim_cfg.yml and a \"patched\" ISO was pushed out.
    In this case, you will see 3 files."
    outLog "Please take a look at the folder you pased in and remove any excessive files/folders and re-run using the same cli parameters."
    exit 1
  fi

  if [[ ( ! -e ${_bake_folder_}/8000.qcow2 ) && ( ! -e ${_bake_folder_}/8808 ) ]];
  then
    outLog "Finding resulting qcow2 file"
    for f in $(realpath "${_bake_folder_}"/* 2>/dev/null);
    do
      _ext_="${f##*.}"
      if [[ "${_ext_}" == "iso" ]];
      then
        continue
      fi

      if [[ "${_ext_}" == "qcow2" ]];
      then
        outLog "Found qcow2 file: ${f}"
        _cur_basename_="$(basename -- ${f})"
        _new_basename_="8000.qcow2"
        outLog "Renaming ${_cur_basename_} to 8000.qcow2"
        mv "${_bake_folder_}/${_cur_basename_}" "${_bake_folder_}/${_new_basename_}"
        [[ $? -ne 0 ]] && outLog "ERROR: Failed to move ${_bake_folder_}/${_cur_basename_} to ${_bake_folder_}/${_new_basename_}. Exiting" && exit 1
      fi
    done
  fi

  if [[ "${PLAT_TO_BUILD::2}" != "88" ]]; then
      [[ ! -e ${_bake_folder_}/8000.qcow2 ]] && outLog "ERROR: Could not find 8000.qcow2. Exiting" && exit 1
  fi
else 
  outLog "-n was passed in, so skipping the baking process. Resulting docker image will boot from ISO instead of qcow2"
fi

outLog "Creating cmd to build docker component"
_iso_boot_cmd_arg_=""
[[ ${NO_BAKE} -eq 0 ]] && _iso_boot_cmd_arg_="-i"

_cmd_="${_build_clab_component_script_} -p ${PLAT_TO_BUILD} ${_iso_boot_cmd_arg_}"

if [[ "${PLAT_TO_BUILD::2}" == "88" ]];
then
  #cmd to make docker component for sf-d
  _cmd_="${_cmd_} -d ${_bake_folder_}"
else
    #extract npsuite/sdk version from iso#
    which isoinfo > /dev/null 2>&1
    [[ $? -ne 0 ]] && echo "/usr/bin/isoinfo missing. Please install rpm genisoimage" && exit 1
    _getisoinfo_="isoinfo -R -x /sim_cfg.yml -i "
    SDK_VER="$(${_getisoinfo_} ${_iso_drop_path_} | grep sdk: | awk '{print $2}')"
    [[ ! ${SDK_VER} ]] && SDK_VER="$(${_getisoinfo_} ${_iso_drop_path_} | grep sdk_ver_pacific: | awk '{print $2}')"
    [[ ! ${SDK_VER} ]] && echo "Failed to extract SDK VERSION from iso ${_iso_drop_path_}" && echo "Once you find the version, call this manually: ${_cmd_} -f ${_bake_folder_} -s <SDK_VER> -n <NPSUITE_VER> ${DOCKER_NAME}" && fail_exit
    NPSUITE_VER="$(${_getisoinfo_} ${_iso_drop_path_} | grep npsuite: | awk '{print $2}')"
    [[ ! ${NPSUITE_VER} ]] && NPSUITE_VER="$(${_getisoinfo_} ${_iso_drop_path_} | grep npsuite_ver_pacific: | awk '{print $2}')"
    [[ ! ${NPSUITE_VER} ]] && echo "Failed to extract NPSUITE VERSION from iso ${_iso_drop_path_}" && echo "Once you find the version, call this manually: ${_cmd_} -f ${_bake_folder_} -s <SDK_VER> -n <NPSUITE_VER> ${DOCKER_NAME}" && fail_exit
    
    if [[ "${PLAT_TO_BUILD}" == "8K-MPA-16H" ]];
    then
      #cmd to make docker component for sf-c
      _cmd_="${_cmd_} -c ${_bake_folder_}"
    else
      #cmd to make docker component for sf-f
      _cmd_="${_cmd_} -f ${_bake_folder_}"
    fi
    _cmd_="${_cmd_} -s ${SDK_VER} -n ${NPSUITE_VER}"
fi
_cmd_="${_cmd_} ${DOCKER_NAME}"
outLog "cmd: ${_cmd_}"

eval ${_cmd_}
exit
