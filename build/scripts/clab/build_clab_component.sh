# CHECK ENC_OFF IN ISO START #
function check_enc_off_in_iso() {
  local iso_file="$1"
  
  # Check if ISO file exists
  if [[ ! -f "$iso_file" ]]; then
    outLog "ERROR: ISO file $iso_file does not exist"
    return 1
  fi
  
  # Check if isoinfo is available
  if ! command -v isoinfo >/dev/null 2>&1; then
    outLog "ERROR: isoinfo command not found. Please install genisoimage package"
    return 1
  fi
  
  # Extract sim_cfg.yml from ISO and check for enc_off=1
  local sim_cfg_content
  sim_cfg_content=$(isoinfo -R -x /sim_cfg.yml -i "$iso_file" 2>/dev/null)
  
  if [[ $? -ne 0 ]]; then
    outLog "WARNING: Failed to extract sim_cfg.yml from $iso_file"
    return 1
  fi
  
  # Check if enc_off=1 is present in the content
  if echo "$sim_cfg_content" | grep -q "enc_off.*=.*1"; then
    outLog "Found enc_off=1 in sim_cfg.yml of $iso_file"
    return 0
  else
    outLog "enc_off=1 not found in sim_cfg.yml of $iso_file"
    return 1
  fi
}
