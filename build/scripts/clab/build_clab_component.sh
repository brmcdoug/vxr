# CREATE BOOTABLE ISO WITH SAME SIM_CFG START #
function create_bootable_iso_with_sim_cfg() {
  local source_iso="$1"
  local target_iso="$2"
  local sdk_ver="$3"
  local npsuite_ver="$4"
  
  outLog "Creating blank boot ISO with same sim_cfg.yml for KNE usage"
  
  # Extract sim_cfg.yml from the original ISO
  local temp_sim_cfg="/tmp/sim_cfg_${HOSTNAME}_$$_$(date +%s).yml"
  isoinfo -R -x /sim_cfg.yml -i "${source_iso}" > "${temp_sim_cfg}" 2>/dev/null
  if [[ $? -ne 0 ]]; then
    outLog "WARNING: Failed to extract sim_cfg.yml from ${source_iso}, using default values"
    rm -f "${temp_sim_cfg}"
    temp_sim_cfg=""
  fi
  
  # Locate create_empty_bootable_iso.sh script
  local create_iso_script="${_scripts_folder_}/bin/create_empty_bootable_iso.sh"
  if [[ ! -e "${create_iso_script}" ]]; then
    outLog "ERROR: create_empty_bootable_iso.sh not found at ${create_iso_script}"
    rm -f "${temp_sim_cfg}"
    fail_exit
  fi
  
  # Create bootable ISO
  if [[ -n "${temp_sim_cfg}" && -f "${temp_sim_cfg}" ]]; then
    # Use extracted sim_cfg.yml
    outLog "Creating bootable ISO using extracted sim_cfg.yml from original ISO"
    "${create_iso_script}" "${target_iso}" "${temp_sim_cfg}"
  else
    # Use SDK and NPSUITE versions
    outLog "Creating bootable ISO using SDK ${sdk_ver} and NPSUITE ${npsuite_ver}"
    "${create_iso_script}" "${target_iso}" "${sdk_ver}" "${npsuite_ver}"
  fi
  
  if [[ $? -ne 0 ]]; then
    outLog "ERROR: Failed to create bootable ISO"
    rm -f "${temp_sim_cfg}"
    fail_exit
  fi
  
  # Clean up temporary sim_cfg.yml
  rm -f "${temp_sim_cfg}"
  outLog "Successfully created bootable ISO at ${target_iso}"
}
