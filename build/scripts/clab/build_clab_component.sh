if [[ "${PLAT_TO_BUILD::2}" == "88" ]];
then
  sed -i "s/_NPSUITE_VERSION_FILLER_/${NPSUITE_VER}/g" ${_8808_drop_path_}
  [ $? -ne 0 ] && fail_exit
  sed -i "s/_SDK_VERSION_FILLER_/${SDK_VER}/g" ${_8808_drop_path_}
  [ $? -ne 0 ] && fail_exit
  sed -i "s/_PLAT_TYPE_FILLER_/${PLAT_TO_BUILD}/g" ${_8808_drop_path_}
else
  sed -i "s/_NPSUITE_VERSION_FILLER_/${NPSUITE_VER}/g" ${DROP_PATH_TGT}
  [ $? -ne 0 ] && fail_exit
  sed -i "s/_SDK_VERSION_FILLER_/${SDK_VER}/g" ${DROP_PATH_TGT}
  [ $? -ne 0 ] && fail_exit
  sed -i "s/_PLAT_TYPE_FILLER_/${PLAT_TO_BUILD}/g" ${DROP_PATH_TGT}
fi

#Build docker now that preperations are all done
if [[ ${SKIP_DOCKER_BUILD} -eq 0 ]];
then
  outLog "Skipping docker build step - skip_docker_build was passed in"
else
  outLog "Building docker image"
  _CMD_="${_docker_build_script_} -i ${SDK_VER} -c ${DOCKER_NAME} ${DOCKERFILE_TO_BUILD}"
  outLog "Calling cmd: ${_CMD_}"
  eval "${_CMD_}"
  [ $? -ne 0 ] && echo "FAILED TO BUILD THE DOCKER IMAGE." && exit 1

  outLog "Successfully built docker image"
  docker image ls ${DOCKER_NAME}

  _tar_name_="${DOCKER_NAME}.tar"
  outLog "Exporting docker image to ${IMG_FOLDER}/${_tar_name_}"
  docker save ${DOCKER_NAME} > ${IMG_FOLDER}/${_tar_name_}
  chmod 666 ${IMG_FOLDER}/${_tar_name_}
  if [ $? -ne 0 ]; then
    outLog "Failed to chmod 666 ${IMG_FOLDER}/${_tar_name_}"
    exit 1
  else
    outLog "Successfully exported docker image to ${IMG_FOLDER}/${_tar_name_}"
    exit 0
  fi
fi

