#Return: 0 if it is old enough, 1 if not
function isItOldEnough() {
  _FILE_=$1
  _AGE_=$2
  [[ "${_AGE_}" == "" ]] && _AGE_="720"
  if [[ -e "${_FILE_}" ]];
  then
    if [[ "$(find ${_FILE_} -mmin +${_AGE_})" != "" ]];
    then
      return 0
    fi
  fi
  return 1
}
